bool IfBinaryRepresentationOfTwoNumbersAreAnagram(int m,int n)
{
	int cnt=0;
	while(m)
	{
		cnt+=m%2;
		m/=2;
	}
	while(n)
	{
		cnt-=n%2;
		n/=2;
	}
	return !cnt;
}

